export type TranslationKey = keyof typeof en;

const enTable = en as unknown as Record<TranslationKey, string>;
const arTable = ar as unknown as Record<TranslationKey, string>;

/** Fully materialized per-locale tables (EN base + overrides for all other locales). */
export const translations = Object.fromEntries(
  CORE_LOCALES.map(({ code }) => [
    code,
    buildLocaleTable(code, enTable, arTable),
  ])
) as Record<Locale, Record<TranslationKey, string>>;

/**
 * Resolve copy for the active locale. Missing or empty values fall back to English,
 * then to the key name so the UI never shows a blank label.
 */
export function resolveTranslation(locale: Locale, key: TranslationKey): string {
  const table = translations[locale] ?? translations.en;
  const localized = table[key];
  if (typeof localized === "string" && localized.length > 0) {
    return localized;
  }
  const fallback = translations.en[key];
  if (typeof fallback === "string" && fallback.length > 0) {
    return fallback;
  }
  return String(key);
}
